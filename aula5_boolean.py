# Tipos de dados - bool (boolean)
'''
Ao questionar algo ao programa, existem duas respostas
Sim(False), Não(False)

Existem varios operadores para "questionar"
== -> questiona se um valor é igual ao outro

'''

print(11 == 10) # False - não
print(10 == 10) # True - sim

print(type(10 == 10)) # True - tipo booleano

# True / False -> bool
print(type(True))
print(type(False))

print(type('False'))