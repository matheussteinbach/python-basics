# Tipos int e float
# int -> numero inteiro, positivo ou negativo

print(11) # int
print(-11) # numero literal, literalmente escrevendo
print(0) # int

# float -> numero com ponto flutuante, casa decimal
# representa qualquer numero

print(1.1) # ponto separa a casa decimal, vírgula separa os argumentos
print(0.0, -1.5) # tipo float

# A função type mostra o tipo (na real classe)
# tudo com letra minuscula e é executavel -> callable / função

print(type('Matheus'))
print(type(0.0))
print(type('0.0'))